    private void hamtaUppgifter() {
    try {
        String sql = 
    "SELECT fornamn, efternamn, epost, telefon, adress " +
    "FROM anstalld " +
    "WHERE aid = " + aid;

        var rad = idb.fetchRow(sql);

        tfNamn.setText(rad.get("fornamn") + " " + rad.get("efternamn"));
        tfEpost.setText(rad.get("epost"));
        tfTelefon.setText(rad.get("telefon"));
        tfAdress.setText(rad.get("adress"));
        

    } catch (InfException ex) {
        JOptionPane.showMessageDialog(this, "Kunde inte h√§mta uppgifter");
    }
}
